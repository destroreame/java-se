package br.com.destroreame.operacoes;

/**
 * Classe que processa operações Matemáticas
 */
public class OperacoesMatematicas {

    /**
     * Soma valor1 e valor2
     * @param valor1 - valor para somar
     * @param valor2 - valor para somar
     * @return Soma dos valores
     */
    public static int somar(int valor1, int valor2) {
        return valor1 + valor2;
    }
}
